def get_vel(pt1, pt2):
	pt1_time = pt1[0]
	pt1_lat = pt1[1]
	pt1_lon = pt1[2]
	pt2_time = pt2[0]
	pt2_lat = pt2[1]
	pt2_lon = pt2[2]
	coord_dist = math.sqrt(math.pow(abs(pt2_lat - pt1_lat),2) +  \
	math.pow(abs(pt2_lon - pt1_lon),2))
	dist = (coord_dist*10000)*36.5 #distance between pts in ft
	#print "point 1 lat:", pt1_lat, "point 1 lon ", pt1_lon, "dist/vel = ", dist
	tim = pt2_time-pt1_time #almost always 1 sec exactly
	vel = ((dist/5280)*3600)/(tim)  #vel in MPH
	
	return vel



